import functools

MOVE = {(1, 0): 'S', (-1, 0): 'N', (0, -1): 'W', (0, 1): 'E'}

def dfs(maze_map, start, goal, path=None):
    path = path or [start]
    if start == goal:
        yield path
    neighbors = [(start[0] + x[0], start[1] + x[1]) for x in MOVE.keys()]
    for next in set([n for n in neighbors if not maze_map[n[0]][n[1]]]) - set(path):
        yield from dfs(maze_map, next, goal, path + [next])

def checkio(maze_map):
    path = next(dfs(maze_map, (1, 1), (10, 10)))

    return functools.reduce(
        lambda r, x: (x, r[1] + ("" if not r[0] else MOVE[(x[0] - r[0][0], x[1] - r[0][1])])),
        path,
        (None, "")
    )[1]


